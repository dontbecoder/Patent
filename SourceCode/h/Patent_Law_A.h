//第一章 总则

#define A1	//第一条　为了保护专利权人的合法权益，鼓励发明创造，推动发明创造的应用，提高创新能力，促进科学技术进步和经济社会发展，制定本法。

#define A2	//第二条　本法所称的发明创造是指发明、实用新型和外观设计。

						//发明，是指对产品、方法或者其改进所提出的新的技术方案。

						//实用新型，是指对产品的形状、构造或者其结合所提出的适于实用的新的技术方案。

						//外观设计，是指对产品的形状、图案或者其结合以及色彩与形状、图案的结合所作出的富有美感并适于工业应用的新设计。

#define A3	//第三条　国务院专利行政部门负责管理全国的专利工作;统一受理和审查专利申请，依法授予专利权。省、自治区、直辖市人民政府管理专利工作的部门负责本行政区域内的专利管理工作。

#define A4	//第四条　申请专利的发明创造涉及国家安全或者重大利益需要保密的，按照国家有关规定办理。

#define A5	//第五条　对违反法律、社会公德或者妨害公共利益的发明创造，不授予专利权。

						//对违反法律、行政法规的规定获取或者利用遗传资源，并依赖该遗传资源完成的发明创造，不授予专利权。

#define A6	//第六条　执行本单位的任务或者主要是利用本单位的物质技术条件所完成的发明创造为职务发明创造。

						//职务发明创造申请专利的权利属于该单位;申请被批准后，该单位为专利权人。

						//非职务发明创造，申请专利的权利属于发明人或者设计人;申请被批准后，该发明人或者设计人为专利权人。

						//利用本单位的物质技术条件所完成的发明创造，单位与发明人或者设计人订有合同，对申请专利的权利和专利权的归属作出约定的，从其约定。

#define A7	//第七条　对发明人或者设计人的非职务发明创造专利申请，任何单位或者个人不得压制。

#define A8	//第八条　两个以上单位或者个人合作完成的发明创造、一个单位或者个人接受其他单位或者个人委托所完成的发明创造，除另有协议的以外，申请专利的权利属于完成或者共同完成的单位或者个人;

						//申请被批准后，申请的单位或者个人为专利权人。

#define A9	//第九条　同样的发明创造只能授予一项专利权。

						//但是，同一申请人同日对同样的发明创造既申请实用新型专利又申请发明专利，先获得的实用新型专利权尚未终止，且申请人声明放弃该实用新型专利权的，可以授予发明专利权。

						//两个以上的申请人分别就同样的发明创造申请专利的，专利权授予最先申请的人。

#define A10	//第十条　专利申请权和专利权可以转让。

						//中国单位或者个人向外国人、外国企业或者外国其他组织转让专利申请权或者专利权的，应当依照有关法律、行政法规的规定办理手续。

						//转让专利申请权或者专利权的，当事人应当订立书面合同，并向国务院专利行政部门登记，由国务院专利行政部门予以公告。

						//专利申请权或者专利权的转让自登记之日起生效。

#define A11	//第十一条　发明和实用新型专利权被授予后，除本法另有规定的以外，任何单位或者个人未经专利权人许可，都不得实施其专利，即不得为生产经营目的制造、使用、许诺销售、销售、进口其专利产品，或者使用其专利方法以及使用、许诺销售、销售、进口依照该专利方法直接获得的产品。

						//外观设计专利权被授予后，任何单位或者个人未经专利权人许可，都不得实施其专利，即不得为生产经营目的制造、许诺销售、销售、进口其外观设计专利产品。

#define A12	//第十二条　任何单位或者个人实施他人专利的，应当与专利权人订立实施许可合同，向专利权人支付专利使用费。被许可人无权允许合同规定以外的任何单位或者个人实施该专利。

#define A13	//第十三条　发明专利申请公布后，申请人可以要求实施其发明的单位或者个人支付适当的费用。

#define A14	//第十四条　国有企业事业单位的发明专利，对国家利益或者公共利益具有重大意义的，国务院有关主管部门和省、自治区、直辖市人民政府报经国务院批准，可以决定在批准的范围内推广应用，允许指定的单位实施，由实施单位按照国家规定向专利权人支付使用费。

#define A15	//第十五条　专利申请权或者专利权的共有人对权利的行使有约定的，从其约定。没有约定的，共有人可以单独实施或者以普通许可方式许可他人实施该专利;

							//许可他人实施该专利的，收取的使用费应当在共有人之间分配。

							//除前款规定的情形外，行使共有的专利申请权或者专利权应当取得全体共有人的同意。

#define A16	//第十六条　被授予专利权的单位应当对职务发明创造的发明人或者设计人给予奖励;发明创造专利实施后，根据其推广应用的范围和取得的经济效益，对发明人或者设计人给予合理的报酬。

#define A17	//第十七条　发明人或者设计人有权在专利文件中写明自己是发明人或者设计人。专利权人有权在其专利产品或者该产品的包装上标明专利标识。

#define A18	//第十八条　在中国没有经常居所或者营业所的外国人、外国企业或者外国其他组织在中国申请专利的，依照其所属国同中国签订的协议或者共同参加的国际条约，或者依照互惠原则，根据本法办理。

#define A19	//第十九条　在中国没有经常居所或者营业所的外国人、外国企业或者外国其他组织在中国申请专利和办理其他专利事务的，应当委托依法设立的专利代理机构办理。

						 //中国单位或者个人在国内申请专利和办理其他专利事务的，可以委托依法设立的专利代理机构办理。

						 //专利代理机构应当遵守法律、行政法规，按照被代理人的委托办理专利申请或者其他专利事务;对被代理人发明创造的内容，除专利申请已经公布或者公告的以外，负有保密责任。

						 //专利代理机构的具体管理办法由国务院规定。

#define A20	//第二十条　任何单位或者个人将在中国完成的发明或者实用新型向外国申请专利的，应当事先报经国务院专利行政部门进行保密审查。保密审查的程序、期限等按照国务院的规定执行。

						 //中国单位或者个人可以根据中华人民共和国参加的有关国际条约提出专利国际申请。申请人提出专利国际申请的，应当遵守前款规定。国务院专利行政部门依照中华人民共和国参加的有关国际条约、本法和国务院有关规定处理专利国际申请。

						 //对违反本条第一款规定向外国申请专利的发明或者实用新型，在中国申请专利的，不授予专利权。

#define A21	//第二十一条　国务院专利行政部门及其专利复审委员会应当按照客观、公正、准确、及时的要求，依法处理有关专利的申请和请求。国务院专利行政部门应当完整、准确、及时发布专利信息，定期出版专利公报。在专利申请公布或者公告前，国务院专利行政部门的工作人员及有关人员对其内容负有保密责任。


//第二章 授予专利权的条件


#define A22	//第二十二条　授予专利权的发明和实用新型，应当具备新颖性、创造性和实用性。

						  //新颖性，是指该发明或者实用新型不属于现有技术;也没有任何单位或者个人就同样的发明或者实用新型在申请日以前向国务院专利行政部门提出过申请，并记载在申请日以后公布的专利申请文件或者公告的专利文件中。

						  //创造性，是指与现有技术相比，该发明具有突出的实质性特点和显著的进步，该实用新型具有实质性特点和进步。

						  //实用性，是指该发明或者实用新型能够制造或者使用，并且能够产生积极效果。本法所称现有技术，是指申请日以前在国内外为公众所知的技术。

#define A23	//第二十三条　授予专利权的外观设计，应当不属于现有设计;也没有任何单位或者个人就同样的外观设计在申请日以前向国务院专利行政部门提出过申请，并记载在申请日以后公告的专利文件中。

						 	//授予专利权的外观设计与现有设计或者现有设计特征的组合相比，应当具有明显区别。

							//授予专利权的外观设计不得与他人在申请日以前已经取得的合法权利相冲突。

							//本法所称现有设计，是指申请日以前在国内外为公众所知的设计。

#define A24	//第二十四条　申请专利的发明创造在申请日以前六个月内，有下列情形之一的，不丧失新颖性：

#define A24_1	//(一)在中国政府主办或者承认的国际展览会上首次展出的;

#define A24_2	//(二)在规定的学术会议或者技术会议上首次发表的;

#define A24_3	//(三)他人未经申请人同意而泄露其内容的。

#define A25	//第二十五条　对下列各项，不授予专利权：

#define A25_1	//(一)科学发现;

#define A25_2	//(二)智力活动的规则和方法;

#define A25_3	//(三)疾病的诊断和治疗方法;

#define A25_4	//(四)动物和植物品种;

#define A25_5	//(五)用原子核变换方法获得的物质;

#define A25_6	//(六)对平面印刷品的图案、色彩或者二者的结合作出的主要起标识作用的设计。对前款第(四)项所列产品的生产方法，可以依照本法规定授予专利权。



//第三章 专利的申请


#define A26	//第二十六条　申请发明或者实用新型专利的，应当提交请求书、说明书及其摘要和权利要求书等文件。

							//请求书应当写明发明或者实用新型的名称，发明人的姓名，申请人姓名或者名称、地址，以及其他事项。

							//说明书应当对发明或者实用新型作出清楚、完整的说明，以所属技术领域的技术人员能够实现为准;必要的时候，应当有附图。

							//摘要应当简要说明发明或者实用新型的技术要点。

							//权利要求书应当以说明书为依据，清楚、简要地限定要求专利保护的范围。依赖遗传资源完成的发明创造，申请人应当在专利申请文件中说明该遗传资源的直接来源和原始来源;申请人无法说明原始来源的，应当陈述理由。

#define A27	//第二十七条　申请外观设计专利的，应当提交请求书、该外观设计的图片或者照片以及对该外观设计的简要说明等文件。申请人提交的有关图片或者照片应当清楚地显示要求专利保护的产品的外观设计。

#define A28	//第二十八条　国务院专利行政部门收到专利申请文件之日为申请日。如果申请文件是邮寄的，以寄出的邮戳日为申请日。

#define A29	//第二十九条　申请人自发明或者实用新型在外国第一次提出专利申请之日起十二个月内，或者自外观设计在外国第一次提出专利申请之日起六个月内，又在中国就相同主题提出专利申请的，依照该外国同中国签订的协议或者共同参加的国际条约，或者依照相互承认优先权的原则，可以享有优先权。申请人自发明或者实用新型在中国第一次提出专利申请之日起十二个月内，又向国务院专利行政部门就相同主题提出专利申请的，可以享有优先权。

#define A30	//第三十条　申请人要求优先权的，应当在申请的时候提出书面声明，并且在三个月内提交第一次提出的专利申请文件的副本;未提出书面声明或者逾期未提交专利申请文件副本的，视为未要求优先权。

#define A31	//第三十一条　一件发明或者实用新型专利申请应当限于一项发明或者实用新型。

							//属于一个总的发明构思的两项以上的发明或者实用新型，可以作为一件申请提出。

							//一件外观设计专利申请应当限于一项外观设计。同一产品两项以上的相似外观设计，或者用于同一类别并且成套出售或者使用的产品的两项以上外观设计，可以作为一件申请提出。

#define A32	//第三十二条　申请人可以在被授予专利权之前随时撤回其专利申请。

#define A33	//第三十三条　申请人可以对其专利申请文件进行修改，但是，对发明和实用新型专利申请文件的修改不得超出原说明书和权利要求书记载的范围，对外观设计专利申请文件的修改不得超出原图片或者照片表示的范围。



//第四章 专利申请的审查和批准


#define A34	//第三十四条　国务院专利行政部门收到发明专利申请后，经初步审查认为符合本法要求的，自申请日起满十八个月，即行公布。国务院专利行政部门可以根据申请人的请求早日公布其申请。

#define A35	//第三十五条　发明专利申请自申请日起三年内，国务院专利行政部门可以根据申请人随时提出的请求，对其申请进行实质审查;

				//申请人无正当理由逾期不请求实质审查的，该申请即被视为撤回。

				//国务院专利行政部门认为必要的时候，可以自行对发明专利申请进行实质审查。

#define A36	//第三十六条　发明专利的申请人请求实质审查的时候，应当提交在申请日前与其发明有关的参考资料。发明专利已经在外国提出过申请的，国务院专利行政部门可以要求申请人在指定期限内提交该国为审查其申请进行检索的资料或者审查结果的资料;无正当理由逾期不提交的，该申请即被视为撤回。

#define A37	//第三十七条　国务院专利行政部门对发明专利申请进行实质审查后，认为不符合本法规定的，应当通知申请人，要求其在指定的期限内陈述意见，或者对其申请进行修改;无正当理由逾期不答复的，该申请即被视为撤回。

#define A38	//第三十八条　发明专利申请经申请人陈述意见或者进行修改后，国务院专利行政部门仍然认为不符合本法规定的，应当予以驳回。

#define A39	//第三十九条　发明专利申请经实质审查没有发现驳回理由的，由国务院专利行政部门作出授予发明专利权的决定，发给发明专利证书，同时予以登记和公告。发明专利权自公告之日起生效。

#define A40	//第四十条　实用新型和外观设计专利申请经初步审查没有发现驳回理由的，由国务院专利行政部门作出授予实用新型专利权或者外观设计专利权的决定，发给相应的专利证书，同时予以登记和公告。实用新型专利权和外观设计专利权自公告之日起生效。

#define A41	//第四十一条　国务院专利行政部门设立专利复审委员会。专利申请人对国务院专利行政部门驳回申请的决定不服的，可以自收到通知之日起三个月内，向专利复审委员会请求复审。专利复审委员会复审后，作出决定，并通知专利申请人。专利申请人对专利复审委员会的复审决定不服的，可以自收到通知之日起三个月内向人民法院起诉。




//第五章 专利权的期限、终止和无效


#define A42	//第四十二条　发明专利权的期限为二十年，实用新型专利权和外观设计专利权的期限为十年，均自申请日起计算。

#define A43	//第四十三条　专利权人应当自被授予专利权的当年开始缴纳年费。

#define A44	//第四十四条　有下列情形之一的，专利权在期限届满前终止：

#define A44_1	//(一)没有按照规定缴纳年费的;

#define A44_2	//(二)专利权人以书面声明放弃其专利权的。专利权在期限届满前终止的，由国务院专利行政部门登记和公告。

#define A45	//第四十五条　自国务院专利行政部门公告授予专利权之日起，任何单位或者个人认为该专利权的授予不符合本法有关规定的，可以请求专利复审委员会宣告该专利权无效。

#define A46	//第四十六条　专利复审委员会对宣告专利权无效的请求应当及时审查和作出决定，并通知请求人和专利权人。宣告专利权无效的决定，由国务院专利行政部门登记和公告。对专利复审委员会宣告专利权无效或者维持专利权的决定不服的，可以自收到通知之日起三个月内向人民法院起诉。人民法院应当通知无效宣告请求程序的对方当事人作为第三人参加诉讼。

#define A47	//第四十七条　宣告无效的专利权视为自始即不存在。宣告专利权无效的决定，对在宣告专利权无效前人民法院作出并已执行的专利侵权的判决、调解书，已经履行或者强制执行的专利侵权纠纷处理决定，以及已经履行的专利实施许可合同和专利权转让合同，不具有追溯力。但是因专利权人的恶意给他人造成的损失，应当给予赔偿。依照前款规定不返还专利侵权赔偿金、专利使用费、专利权转让费，明显违反公平原则的，应当全部或者部分返还。




//第六章 专利实施的强制许可


#define A48	//第四十八条　有下列情形之一的，国务院专利行政部门根据具备实施条件的单位或者个人的申请，可以给予实施发明专利或者实用新型专利的强制许可：

#define A48_1	//(一)专利权人自专利权被授予之日起满三年，且自提出专利申请之日起满四年，无正当理由未实施或者未充分实施其专利的;

#define A48_2	//(二)专利权人行使专利权的行为被依法认定为垄断行为，为消除或者减少该行为对竞争产生的不利影响的。

#define A49	//第四十九条　在国家出现紧急状态或者非常情况时，或者为了公共利益的目的，国务院专利行政部门可以给予实施发明专利或者实用新型专利的强制许可。

#define A50	//第五十条　为了公共健康目的，对取得专利权的药品，国务院专利行政部门可以给予制造并将其出口到符合中华人民共和国参加的有关国际条约规定的国家或者地区的强制许可。

#define A51	//第五十一条　一项取得专利权的发明或者实用新型比前已经取得专利权的发明或者实用新型具有显著经济意义的重大技术进步，其实施又有赖于前一发明或者实用新型的实施的，国务院专利行政部门根据后一专利权人的申请，可以给予实施前一发明或者实用新型的强制许可。在依照前款规定给予实施强制许可的情形下，国务院专利行政部门根据前一专利权人的申请，也可以给予实施后一发明或者实用新型的强制许可。

#define A52	//第五十二条　强制许可涉及的发明创造为半导体技术的，其实施限于公共利益的目的和本法第四十八条第(二)项规定的情形。

#define A53	//第五十三条　除依照本法第四十八条第(二)项、第五十条规定给予的强制许可外，强制许可的实施应当主要为了供应国内市场。

#define A54	//第五十四条　依照本法第四十八条第(一)项、第五十一条规定申请强制许可的单位或者个人应当提供证据，证明其以合理的条件请求专利权人许可其实施专利，但未能在合理的时间内获得许可。

#define A55	//第五十五条　国务院专利行政部门作出的给予实施强制许可的决定，应当及时通知专利权人，并予以登记和公告。给予实施强制许可的决定，应当根据强制许可的理由规定实施的范围和时间。强制许可的理由消除并不再发生时，国务院专利行政部门应当根据专利权人的请求，经审查后作出终止实施强制许可的决定。

#define A56	//第五十六条　取得实施强制许可的单位或者个人不享有独占的实施权，并且无权允许他人实施。

#define A57	//第五十七条　取得实施强制许可的单位或者个人应当付给专利权人合理的使用费，或者依照中华人民共和国参加的有关国际条约的规定处理使用费问题。付给使用费的，其数额由双方协商;双方不能达成协议的，由国务院专利行政部门裁决。

#define A58	//第五十八条　专利权人对国务院专利行政部门关于实施强制许可的决定不服的，专利权人和取得实施强制许可的单位或者个人对国务院专利行政部门关于实施强制许可的使用费的裁决不服的，可以自收到通知之日起三个月内向人民法院起诉。




//第七章 专利权的保护


#define A59	//第五十九条　发明或者实用新型专利权的保护范围以其权利要求的内容为准，说明书及附图可以用于解释权利要求的内容。

						//外观设计专利权的保护范围以表示在图片或者照片中的该产品的外观设计为准，简要说明可以用于解释图片或者照片所表示的该产品的外观设计。

#define A60	//第六十条　未经专利权人许可，实施其专利，即侵犯其专利权，引起纠纷的，由当事人协商解决;不愿协商或者协商不成的，专利权人或者利害关系人可以向人民法院起诉，也可以请求管理专利工作的部门处理。管理专利工作的部门处理时，认定侵权行为成立的，可以责令侵权人立即停止侵权行为，当事人不服的，可以自收到处理通知之日起十五日内依照《中华人民共和国行政诉讼法》向人民法院起诉;侵权人期满不起诉又不停止侵权行为的，管理专利工作的部门可以申请人民法院强制执行。进行处理的管理专利工作的部门应当事人的请求，可以就侵犯专利权的赔偿数额进行调解;调解不成的，当事人可以依照《中华人民共和国民事诉讼法》向人民法院起诉。

#define A61	//第六十一条　专利侵权纠纷涉及新产品制造方法的发明专利的，制造同样产品的单位或者个人应当提供其产品制造方法不同于专利方法的证明。专利侵权纠纷涉及实用新型专利或者外观设计专利的，人民法院或者管理专利工作的部门可以要求专利权人或者利害关系人出具由国务院专利行政部门对相关实用新型或者外观设计进行检索、分析和评价后作出的专利权评价报告，作为审理、处理专利侵权纠纷的证据。

#define A62	//第六十二条　在专利侵权纠纷中，被控侵权人有证据证明其实施的技术或者设计属于现有技术或者现有设计的，不构成侵犯专利权。

#define A63	//第六十三条　假冒专利的，除依法承担民事责任外，由管理专利工作的部门责令改正并予公告，没收违法所得，可以并处违法所得四倍以下的罚款;没有违法所得的，可以处二十万元以下的罚款;构成犯罪的，依法追究刑事责任。

#define A64	//第六十四条　管理专利工作的部门根据已经取得的证据，对涉嫌假冒专利行为进行查处时，可以询问有关当事人，调查与涉嫌违法行为有关的情况;对当事人涉嫌违法行为的场所实施现场检查;查阅、复制与涉嫌违法行为有关的合同、发票、账簿以及其他有关资料;检查与涉嫌违法行为有关的产品，对有证据证明是假冒专利的产品，可以查封或者扣押。管理专利工作的部门依法行使前款规定的职权时，当事人应当予以协助、配合，不得拒绝、阻挠。

#define A65	//第六十五条　侵犯专利权的赔偿数额按照权利人因被侵权所受到的实际损失确定;实际损失难以确定的，可以按照侵权人因侵权所获得的利益确定。权利人的损失或者侵权人获得的利益难以确定的，参照该专利许可使用费的倍数合理确定。赔偿数额还应当包括权利人为制止侵权行为所支付的合理开支。权利人的损失、侵权人获得的利益和专利许可使用费均难以确定的，人民法院可以根据专利权的类型、侵权行为的性质和情节等因素，确定给予一万元以上一百万元以下的赔偿。

#define A66	//第六十六条　专利权人或者利害关系人有证据证明他人正在实施或者即将实施侵犯专利权的行为，如不及时制止将会使其合法权益受到难以弥补的损害的，可以在起诉前向人民法院申请采取责令停止有关行为的措施。申请人提出申请时，应当提供担保;不提供担保的，驳回申请。人民法院应当自接受申请之时起四十八小时内作出裁定;有特殊情况需要延长的，可以延长四十八小时。裁定责令停止有关行为的，应当立即执行。当事人对裁定不服的，可以申请复议一次;复议期间不停止裁定的执行。申请人自人民法院采取责令停止有关行为的措施之日起十五日内不起诉的，人民法院应当解除该措施。申请有错误的，申请人应当赔偿被申请人因停止有关行为所遭受的损失。

#define A67	//第六十七条　为了制止专利侵权行为，在证据可能灭失或者以后难以取得的情况下，专利权人或者利害关系人可以在起诉前向人民法院申请保全证据。人民法院采取保全措施，可以责令申请人提供担保;申请人不提供担保的，驳回申请。人民法院应当自接受申请之时起四十八小时内作出裁定;裁定采取保全措施的，应当立即执行。申请人自人民法院采取保全措施之日起十五日内不起诉的，人民法院应当解除该措施。

#define A68	//第六十八条　侵犯专利权的诉讼时效为二年，自专利权人或者利害关系人得知或者应当得知侵权行为之日起计算。发明专利申请公布后至专利权授予前使用该发明未支付适当使用费的，专利权人要求支付使用费的诉讼时效为二年，自专利权人得知或者应当得知他人使用其发明之日起计算，但是，专利权人于专利权授予之日前即已得知或者应当得知的，自专利权授予之日起计算。

#define A69	//第六十九条　有下列情形之一的，不视为侵犯专利权：

#define A69_1	//(一)专利产品或者依照专利方法直接获得的产品，由专利权人或者经其许可的单位、个人售出后，使用、许诺销售、销售、进口该产品的;

#define A69_2	//(二)在专利申请日前已经制造相同产品、使用相同方法或者已经作好制造、使用的必要准备，并且仅在原有范围内继续制造、使用的;

#define A69_3	//(三)临时通过中国领陆、领水、领空的外国运输工具，依照其所属国同中国签订的协议或者共同参加的国际条约，或者依照互惠原则，为运输工具自身需要而在其装置和设备中使用有关专利的;

#define A69_4	//(四)专为科学研究和实验而使用有关专利的;

#define A69_5	//(五)为提供行政审批所需要的信息，制造、使用、进口专利药品或者专利医疗器械的，以及专门为其制造、进口专利药品或者专利医疗器械的。

#define A70	//第七十条　为生产经营目的使用、许诺销售或者销售不知道是未经专利权人许可而制造并售出的专利侵权产品，能证明该产品合法来源的，不承担赔偿责任。

#define A71	//第七十一条　违反本法第二十条规定向外国申请专利，泄露国家秘密的，由所在单位或者上级主管机关给予行政处分;构成犯罪的，依法追究刑事责任。

#define A72	//第七十二条　侵夺发明人或者设计人的非职务发明创造专利申请权和本法规定的其他权益的，由所在单位或者上级主管机关给予行政处分。

#define A73	//第七十三条　管理专利工作的部门不得参与向社会推荐专利产品等经营活动。管理专利工作的部门违反前款规定的，由其上级机关或者监察机关责令改正，消除影响，有违法收入的予以没收;情节严重的，对直接负责的主管人员和其他直接责任人员依法给予行政处分。

#define A74	//第七十四条　从事专利管理工作的国家机关工作人员以及其他有关国家机关工作人员玩忽职守、滥用职权、徇私舞弊，构成犯罪的，依法追究刑事责任;尚不构成犯罪的，依法给予行政处分。




